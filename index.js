module.exports = {
  /**
   *
   * @param {*} n1 de la suma
   * @param {*} n2 de la suma
   * @returns number
   */
  suma: function (n1, n2) {
    return n1 + n2;
  },
  resta: function (n1, n2) {
    return n1 - n2;
  },
  multiplicacion: function (n1, n2) {
    return n1 * n2;
  },
  division: function (n1, n2) {
    return n1 / n2;
  },
};
